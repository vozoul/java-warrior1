package Perso;

public class Equipement {

    //offensive equipement
    private String damName;
    private int damAtk;

    public String getDamName() {
        return damName;
    }

    public void setDamName(String damName) {
        this.damName = damName;
    }

    public int getDamAtk() {
        return damAtk;
    }

    public void setDamAtk(int damAtk) {
        this.damAtk = damAtk;
    }
}